// search-box: thin client-side wrapper around the PageFind JS API.
//
// Lifecycle:
//   1. astra build emits the static site to dist/
//   2. `pagefind --site dist` (run as post-step in package.json's `build`
//      script) crawls dist/ and writes the search index under
//      dist/pagefind/. The bundled JS entrypoint lives at
//      `/pagefind/pagefind.js`.
//   3. This Web Component dynamic-imports that bundle at mount time and
//      forwards keystrokes to `pagefind.search(query)`.
//
// Deliberately framework-free — no signals / luna runtime — so the same
// island works regardless of which renderer the host page uses.
class SearchBox extends HTMLElement {
  async connectedCallback() {
    this.innerHTML = `
      <input type="search" placeholder="Search the docs..." aria-label="Search">
      <div class="results" role="listbox" aria-label="Search results"></div>
    `;
    const input = this.querySelector("input");
    const results = this.querySelector(".results");

    let pagefind;
    try {
      // The /pagefind/pagefind.js bundle is generated by `pagefind --site dist`.
      // We import it dynamically so the search UI degrades gracefully
      // when the index hasn't been built yet (e.g. during `astra dev`).
      //
      // The URL is built at runtime against `location.origin` to keep
      // the bundler from rewriting it as a relative path during the
      // islands build — `/pagefind/` only exists at the site root after
      // the post-build `pagefind --site dist` step, not under
      // `/islands/`.
      const url = new URL("/pagefind/pagefind.js", location.origin).href;
      pagefind = await import(/* @vite-ignore */ url);
      if (typeof pagefind.init === "function") {
        await pagefind.init();
      }
    } catch (err) {
      results.innerHTML = `<div class="empty">Search index not available (run <code>pagefind --site dist</code> after build).</div>`;
      return;
    }

    let pending = 0;
    input.addEventListener("input", async (e) => {
      const query = e.target.value.trim();
      const ticket = ++pending;
      if (!query) {
        results.innerHTML = "";
        return;
      }
      const search = await pagefind.search(query);
      // Drop stale responses if the user kept typing.
      if (ticket !== pending) return;

      const top = await Promise.all(
        search.results.slice(0, 5).map((r) => r.data()),
      );
      if (top.length === 0) {
        results.innerHTML = `<div class="empty">No matches for "${escapeHtml(query)}".</div>`;
        return;
      }
      results.innerHTML = top
        .map((r) => {
          const title = escapeHtml(r.meta?.title ?? r.url);
          const excerpt = r.excerpt ?? "";
          return `<a href="${r.url}" role="option">
            <strong>${title}</strong>
            <span class="meta">${excerpt}</span>
          </a>`;
        })
        .join("");
    });
  }
}

function escapeHtml(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

if (!customElements.get("search-box")) {
  customElements.define("search-box", SearchBox);
}
